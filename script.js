var crsr = document.querySelector("#cursor");
var blur_crsr = document.querySelector("#cursor-blur");

document.addEventListener("mousemove", (e) => {
  crsr.style.left = `${e.clientX}px`;
  crsr.style.top = `${e.clientY}px`;
  blur_crsr.style.left = `${e.clientX - 250}px`;
  blur_crsr.style.top = `${e.clientY - 250}px`;
});

var h4all = document.querySelectorAll("#nav h4");
h4all.forEach(function (elm) {
  elm.addEventListener("mouseenter", function () {
    crsr.style.scale = 2;
    crsr.style.border = "1px solid #fff";
    crsr.style.backgroundColor = "transparent";
  });
  elm.addEventListener("mouseleave", function () {
    crsr.style.scale = 1;
    crsr.style.border = "0px solid #95C11E";
    crsr.style.backgroundColor = "#95C11E";
  });
});

